import {whichDay, howManyMin, whoIsOlder} from "./hw-8"

test("Какой день недели?", () => {
    expect(whichDay("12.11.2023")).toBe("Воскресенье");
  });
  
  test("Сколько минут прошло с начала дня?", () => {
    expect(howManyMin(16, 45)).toBe(
      "С начала сегодняшенего дня прошло: 1005 минут.",
    );
  });
  
  test("Кто младше?", () => {
    expect(whoIsOlder("11.01.2001", "06.06.2006")).toBe(
      "Пользователь с ДР 06.06.2006 младше",
    );
  });